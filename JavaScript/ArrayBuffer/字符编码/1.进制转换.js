// 二进制、八进制、十六进制 转换十进制
console.log(12);
console.log(0o12, 0O12, 012, 071); //10 10 10 57
console.log(0x12, 0X12); // 18 18
console.log(0b1001, 0b1001); // 9 9

//十进制转换二进制、八进制、十六进制（字符串）
console.log(100..toString(2)); //1100100
console.log(100..toString(8)); // 144 
console.log(100..toString(16)); //64
